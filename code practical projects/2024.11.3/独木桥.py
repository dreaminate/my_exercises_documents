
    



L=int(input().strip())
N=int(input().strip())
key=[]
for i in range(1,N+1):
    f,s=map(int,input().split())
    if f >0:
        key.append(L-s)
    else:
        key.append(s)
print(max(key))


    

    
    
                
        
    

     
    
    
    
    
       

            
    
    
    
    





    
    
    